def main():
    df = pd.DataFrame(ROWS, columns=["ind_id", "ind_name", "sector", "ksic",
                                     "prod_src", "bok36", "kospi_suffix"])
    assert df["ind_id"].is_unique, "ind_id 중복"
    # KOSPI suffix → 전체 sector_code 매핑
    ks = read_sql("select distinct sector_code from raw_kospi_sector")
    pref = ks["sector_code"].iloc[0].split(".")[0]   # '13102130657A'
    df["kospi_code"] = df["kospi_suffix"].apply(lambda s: f"{pref}.{s.split('.')[1]}" if s else "")
    # 검증: 생산지수 소스에 KSIC 존재 여부
    mfg = set(read_sql("select distinct ksic from raw_mfg_prod")["ksic"])
    svc = set(read_sql("select distinct ksic from raw_svc_prod")["ksic"])
    miss = []
    for _, r in df.iterrows():
        if r["prod_src"] == "mfg" and r["ksic"] not in mfg:
            miss.append(("mfg", r["ind_id"], r["ksic"]))
        if r["prod_src"] == "svc" and r["ksic"] not in svc:
            miss.append(("svc", r["ind_id"], r["ksic"]))
    to_sql(df, "clean_industry_map")
    df.to_csv(os.path.join(DATA, "industry_map_70.csv"), index=False, encoding="utf-8-sig")
    print(f"clean_industry_map: {len(df)}개 산업 | 대분류 {df['sector'].value_counts().to_dict()}")
    print(f"KOSPI 매칭 {(df['kospi_code']!='').sum()} | 생산지수 mfg {(df['prod_src']=='mfg').sum()} svc {(df['prod_src']=='svc').sum()} none {(df['prod_src']=='none').sum()}")
    print("생산지수 KSIC 누락:", miss if miss else "없음")
    # BOK36 부모 커버리지
    parents = set()
    for b in df["bok36"]:
        parents |= set(b.split(";"))
    bok = set(read_sql("select distinct code from raw_va_q")["code"])
    print("BOK36 부모 누락:", sorted(parents - bok) if (parents - bok) else "없음")


if __name__ == "__main__":
    main()
